<h2>What Are Cookies?</h2>

<p>Cookies are small text files that are placed on your device (computer, smartphone, tablet, etc.) when you visit a website. They are widely used to make websites work more efficiently and to provide information to website owners.</p>

<h3>How Cookies Work</h3>
<p>When you visit a website, the website sends a cookie to your browser. Your browser stores the cookie on your device. The next time you visit the same website, your browser sends the cookie back to the website, allowing the website to recognize your device and remember information about your visit.</p>

<h3>Types of Cookies</h3>

<p><strong>Based on Duration:</strong></p>
<ul>
    <li><strong>Session Cookies:</strong> Temporary cookies that are deleted when you close your browser. They are used to remember your actions during a single browsing session, such as items in a shopping cart.</li>
    <li><strong>Persistent Cookies:</strong> Cookies that remain on your device for a set period or until you delete them. They are used to remember your preferences across multiple browsing sessions, such as login status or language preferences.</li>
</ul>

<p><strong>Based on Ownership:</strong></p>
<ul>
    <li><strong>First-Party Cookies:</strong> Cookies set by the website you are visiting. These cookies can only be read by that website.</li>
    <li><strong>Third-Party Cookies:</strong> Cookies set by a domain other than the website you are visiting. These are typically used for advertising and tracking across multiple websites.</li>
</ul>

<h3>Similar Technologies</h3>
<p>In addition to cookies, we may use other similar technologies, including:</p>
<ul>
    <li><strong>Web Beacons (Pixels):</strong> Small graphic images embedded in web pages or emails that track whether you have viewed particular content.</li>
    <li><strong>Local Storage:</strong> A way for websites to store data locally in your browser, similar to cookies but with a larger storage capacity.</li>
    <li><strong>Scripts:</strong> Small programs that run in your browser to collect information about your interactions with the Site.</li>
</ul>

<p>Throughout this Cookie Policy, when we refer to "cookies," we are also referring to these similar technologies.</p>
