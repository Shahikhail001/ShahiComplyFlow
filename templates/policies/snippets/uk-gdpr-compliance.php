<h3>UK GDPR Compliance Statement</h3>
<p>We comply with the UK General Data Protection Regulation (UK GDPR) and the Data Protection Act 2018 for processing personal data of individuals in the United Kingdom.</p>

<h4>Legal Basis for Processing</h4>
<p>We process your personal data under one or more of the following legal bases:</p>
<ul>
    <li><strong>Consent:</strong> You have given clear consent for us to process your personal data for a specific purpose</li>
    <li><strong>Contract:</strong> Processing is necessary for a contract you have with us, or because you have asked us to take specific steps before entering into a contract</li>
    <li><strong>Legal Obligation:</strong> Processing is necessary for us to comply with the law (not including contractual obligations)</li>
    <li><strong>Vital Interests:</strong> Processing is necessary to protect someone's life</li>
    <li><strong>Public Task:</strong> Processing is necessary for us to perform a task in the public interest or for our official functions</li>
    <li><strong>Legitimate Interests:</strong> Processing is necessary for our legitimate interests or the legitimate interests of a third party, unless there is a good reason to protect your personal data which overrides those legitimate interests</li>
</ul>

<h4>Data Protection Officer</h4>
<p>We have appointed a Data Protection Officer (DPO) to oversee compliance with data protection law. If you have any questions about how we handle your personal data, including any requests to exercise your legal rights, please contact our DPO.</p>

<h4>International Data Transfers</h4>
<p>When we transfer personal data outside the UK, we ensure appropriate safeguards are in place, including:</p>
<ul>
    <li>Adequacy decisions by the UK Secretary of State</li>
    <li>Standard Contractual Clauses approved by the ICO</li>
    <li>Binding Corporate Rules</li>
    <li>Approved codes of conduct or certification mechanisms</li>
</ul>

<h4>Supervisory Authority</h4>
<p>You have the right to lodge a complaint with the Information Commissioner's Office (ICO) if you believe we have not handled your personal data lawfully.</p>
