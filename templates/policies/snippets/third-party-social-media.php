<h3>Social Media Integration</h3>
<p>Our Site includes social media features and sharing buttons (such as Facebook, Twitter, LinkedIn, and Instagram). These features may collect your IP address, which page you are visiting on our Site, and may set a cookie to enable the feature to function properly.</p>

<p><strong>Information Shared:</strong></p>
<ul>
    <li>When you use social sharing buttons, information about your visit may be shared with the social media platform</li>
    <li>If you are logged into your social media account, the platform may link your visit to your profile</li>
    <li>Social media platforms may use this information in accordance with their own privacy policies</li>
</ul>

<p><strong>Your Control:</strong></p>
<ul>
    <li>You can manage social media integrations through your social media account settings</li>
    <li>Log out of social media accounts before visiting our Site if you prefer not to have this information collected</li>
    <li>Review the privacy policies of social media platforms you use</li>
</ul>

<p><strong>Note:</strong> These social media features are either hosted by a third party or hosted directly on our Site. Your interactions with these features are governed by the privacy policies of the companies providing them.</p>
