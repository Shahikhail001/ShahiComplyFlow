<h3>LGPD Rights (Brazil)</h3>
<p>If you are a resident of Brazil, you have rights under the Lei Geral de Proteção de Dados (LGPD):</p>
<ul>
    <li><strong>Confirmation of Processing:</strong> You can confirm whether we process your personal data</li>
    <li><strong>Access:</strong> You can access your personal data</li>
    <li><strong>Correction:</strong> You can correct incomplete, inaccurate, or outdated data</li>
    <li><strong>Anonymization or Deletion:</strong> You can request anonymization, blocking, or deletion of unnecessary or excessive data</li>
    <li><strong>Portability:</strong> You can request data portability to another service provider</li>
    <li><strong>Information about Sharing:</strong> You can obtain information about entities with which we share your data</li>
    <li><strong>Refusal:</strong> You can refuse or withdraw consent for data processing</li>
    <li><strong>Review:</strong> You can request review of decisions made solely based on automated processing</li>
</ul>

<p>To exercise your LGPD rights, please contact our Data Protection Officer using the contact information provided.</p>
