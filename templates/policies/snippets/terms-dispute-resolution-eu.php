<h3>European Union Online Dispute Resolution</h3>

<p>If you are a consumer residing in the European Union and you have a dispute with us, you may use the European Commission's Online Dispute Resolution (ODR) platform.</p>

<p><strong>ODR Platform:</strong></p>
<p>The ODR platform is designed to help consumers and traders resolve disputes relating to online purchases of goods and services without going to court. You can access the ODR platform at: <a href="https://ec.europa.eu/consumers/odr/" target="_blank" rel="noopener">https://ec.europa.eu/consumers/odr/</a></p>

<p><strong>How the ODR Platform Works:</strong></p>
<ol>
    <li><strong>Submit Complaint:</strong> You submit your complaint through the ODR platform</li>
    <li><strong>Select ADR Entity:</strong> The platform helps you identify an appropriate Alternative Dispute Resolution (ADR) entity</li>
    <li><strong>Trader Notified:</strong> We are notified of your complaint and invited to participate</li>
    <li><strong>Agree on ADR:</strong> Both parties must agree to use a specific ADR entity</li>
    <li><strong>Resolution Process:</strong> The selected ADR entity conducts the dispute resolution process</li>
    <li><strong>Outcome:</strong> The ADR entity provides a resolution, which may be binding or non-binding depending on the ADR entity's rules</li>
</ol>

<p><strong>Alternative to ODR:</strong></p>
<p>While we are committed to participating in ADR processes through the ODR platform, you are not obligated to use the ODR platform. You may also:</p>
<ul>
    <li>Contact us directly at {{CONTACT_EMAIL}} to resolve the dispute informally</li>
    <li>Contact a consumer advice center in your country</li>
    <li>File a complaint with the relevant regulatory authority in your EU Member State</li>
    <li>Bring legal proceedings in accordance with applicable law</li>
</ul>

<p><strong>Our Commitment:</strong></p>
<p>We are committed to resolving consumer disputes in good faith and will participate in ODR proceedings when requested by consumers. Our goal is to reach a fair resolution efficiently and without the need for court proceedings.</p>

<p><strong>Consumer Rights Not Affected:</strong></p>
<p>Use of the ODR platform or any ADR process does not affect your statutory rights as a consumer under EU law, including your right to bring legal proceedings in court.</p>

