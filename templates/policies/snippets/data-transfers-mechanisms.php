<h3>International Data Transfers</h3>

<p>Your personal data may be transferred to and processed in countries other than your country of residence. These countries may have data protection laws that are different from the laws of your country.</p>

<p><strong>Transfer Mechanisms:</strong></p>
<ul>
    <li><strong>Standard Contractual Clauses (SCCs):</strong> We use the European Commission's approved Standard Contractual Clauses to protect personal data transferred outside the European Economic Area (EEA).</li>
    <li><strong>Adequacy Decisions:</strong> Where possible, we transfer data to countries that have been deemed to provide an adequate level of protection by the European Commission or other relevant authorities.</li>
    <li><strong>Binding Corporate Rules:</strong> Some of our service providers operate under Binding Corporate Rules that provide adequate protection for international data transfers.</li>
    <li><strong>Privacy Shield (Legacy):</strong> For historical transfers to the United States, we relied on the EU-U.S. Privacy Shield framework where applicable, and have since updated our transfer mechanisms.</li>
</ul>

<p><strong>Safeguards:</strong></p>
<p>When transferring your personal data internationally, we implement appropriate safeguards to ensure your data remains protected, including:</p>
<ul>
    <li>Encryption of data in transit and at rest</li>
    <li>Regular security assessments of data recipients</li>
    <li>Contractual obligations requiring recipients to protect your data</li>
    <li>Monitoring of cross-border data flows</li>
</ul>

<p>You can request more information about the specific safeguards we use for international data transfers by contacting us using the details provided in this policy.</p>
