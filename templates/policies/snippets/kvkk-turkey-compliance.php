<h3>KVKK (Turkey) Compliance Statement</h3>
<p>We comply with Turkey's Law on Protection of Personal Data No. 6698 (KVKK - Kişisel Verilerin Korunması Kanunu) for processing personal data of individuals in Turkey.</p>

<h4>General Principles of Data Processing</h4>
<p>We process personal data in accordance with the following principles:</p>
<ul>
    <li><strong>Compliance with Law and Good Faith:</strong> Processing is carried out in compliance with the law and rules of good faith</li>
    <li><strong>Accuracy and Currency:</strong> Personal data is accurate and, where necessary, kept up to date</li>
    <li><strong>Processing for Specific, Explicit, and Legitimate Purposes:</strong> Data is processed for specified, explicit, and legitimate purposes</li>
    <li><strong>Relevance and Limitation:</strong> Data processing is limited to what is relevant with and proportionate to the purposes for which it is processed</li>
    <li><strong>Retention Period:</strong> Data is retained only for the period required by relevant legislation or the purpose for which it is processed</li>
</ul>

<h4>Legal Grounds for Processing</h4>
<p>We process your personal data based on one or more of the following legal grounds:</p>
<ul>
    <li>Your explicit consent</li>
    <li>Explicitly provided for in laws</li>
    <li>Necessary for the protection of life or physical integrity of a person who is physically or legally incapable of giving consent</li>
    <li>Necessary for the establishment, exercise, or protection of a right</li>
    <li>Necessary for our legitimate interests, provided that this does not violate your fundamental rights and freedoms</li>
    <li>Processing of data made public by yourself</li>
</ul>

<h4>Processing of Special Categories of Personal Data</h4>
<p>For special categories of personal data (race, ethnic origin, political opinion, philosophical belief, religion, dress and attire, membership in associations/foundations/trade unions, health, sexual life, criminal convictions, biometric and genetic data), we will only process such data with your explicit consent or in cases explicitly permitted by law.</p>

<h4>Your Rights as a Data Subject</h4>
<p>Under the KVKK, you have the right to:</p>
<ul>
    <li>Learn whether your personal data is being processed</li>
    <li>Request information if your personal data has been processed</li>
    <li>Learn the purpose of processing and whether data is used for its intended purpose</li>
    <li>Know third parties to whom personal data is transferred domestically or abroad</li>
    <li>Request rectification of incomplete or inaccurate data</li>
    <li>Request deletion or destruction of your personal data under certain conditions</li>
    <li>Request notification of rectification, deletion, or destruction to third parties</li>
    <li>Object to negative consequences resulting from analysis of data by automated means</li>
    <li>Claim compensation for damages arising from unlawful processing</li>
</ul>

<h4>Data Controller Notification</h4>
<p>As a data controller processing personal data, we have registered with the Data Controllers Registry (VERBİS) maintained by the Personal Data Protection Authority.</p>

<h4>Information Obligation</h4>
<p>We inform you of the following when collecting your personal data:</p>
<ul>
    <li>Identity of the data controller</li>
    <li>Purpose of processing personal data</li>
    <li>To whom and for what purposes personal data may be transferred</li>
    <li>Method of collection and legal grounds</li>
    <li>Your rights under the KVKK</li>
</ul>

<h4>Data Security</h4>
<p>We take all necessary technical and administrative measures to:</p>
<ul>
    <li>Prevent unlawful processing of personal data</li>
    <li>Prevent unlawful access to data</li>
    <li>Ensure the preservation of data</li>
</ul>

<h4>Transfer of Personal Data</h4>
<p>We may transfer personal data to third parties in Turkey or abroad only:</p>
<ul>
    <li>With your explicit consent, or</li>
    <li>When one of the legal grounds for data processing exists and:
        <ul>
            <li>For transfers within Turkey: When adequate safeguards are provided</li>
            <li>For transfers abroad: When the country has adequate protection or adequate safeguards are provided, and the Personal Data Protection Board authorizes the transfer</li>
        </ul>
    </li>
</ul>

<h4>Data Breach Notification</h4>
<p>In the event of a personal data breach, we will notify:</p>
<ul>
    <li>You, as soon as possible</li>
    <li>The Personal Data Protection Authority, as soon as possible</li>
</ul>

<h4>Application Procedure</h4>
<p>You may submit your requests regarding your rights to our company using the application methods specified in our Information Text on Data Processing and Application Form. We will respond to your application within 30 days, free of charge. However, if the transaction requires additional cost, we may charge a fee based on the tariff determined by the Personal Data Protection Board.</p>

<h4>Personal Data Protection Authority</h4>
<p>If your application is rejected, the response is insufficient, or you do not receive a response within the statutory period, you have the right to file a complaint with the Personal Data Protection Authority.</p>
