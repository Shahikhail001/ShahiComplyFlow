<h2>Managing Cookies</h2>

<p>You have several options for managing cookies on your device. Below are the different ways you can control or delete cookies:</p>

<h3>Browser Settings</h3>
<p>Most web browsers allow you to control cookies through their settings preferences. You can set your browser to:</p>
<ul>
    <li>Block all cookies</li>
    <li>Block third-party cookies only</li>
    <li>Delete cookies when you close your browser</li>
    <li>Notify you when a website tries to set a cookie</li>
    <li>Delete specific cookies</li>
</ul>

<p><strong>How to Manage Cookies in Popular Browsers:</strong></p>
<ul>
    <li><strong>Google Chrome:</strong> Settings → Privacy and security → Cookies and other site data. <a href="https://support.google.com/chrome/answer/95647" target="_blank" rel="noopener">Learn more</a></li>
    <li><strong>Mozilla Firefox:</strong> Settings → Privacy & Security → Cookies and Site Data. <a href="https://support.mozilla.org/en-US/kb/enhanced-tracking-protection-firefox-desktop" target="_blank" rel="noopener">Learn more</a></li>
    <li><strong>Microsoft Edge:</strong> Settings → Cookies and site permissions → Cookies and site data. <a href="https://support.microsoft.com/en-us/microsoft-edge/delete-cookies-in-microsoft-edge-63947406-40ac-c3b8-57b9-2a946a29ae09" target="_blank" rel="noopener">Learn more</a></li>
    <li><strong>Safari:</strong> Preferences → Privacy → Manage Website Data. <a href="https://support.apple.com/guide/safari/manage-cookies-sfri11471/mac" target="_blank" rel="noopener">Learn more</a></li>
    <li><strong>Opera:</strong> Settings → Privacy & Security → Cookies and other site data. <a href="https://help.opera.com/en/latest/web-preferences/#cookies" target="_blank" rel="noopener">Learn more</a></li>
</ul>

<h3>Mobile Device Settings</h3>
<p>If you access our Site from a mobile device, you can manage cookies through your device settings:</p>
<ul>
    <li><strong>iOS (Safari):</strong> Settings → Safari → Block All Cookies or Clear History and Website Data</li>
    <li><strong>Android (Chrome):</strong> Chrome app → Settings → Site settings → Cookies</li>
</ul>

<h3>Do Not Track</h3>
<p>Some browsers include a "Do Not Track" (DNT) feature that signals websites you visit that you do not want to have your online activity tracked. The Site may not respond to DNT signals at this time. However, we provide you with other options to control tracking through our cookie consent banner and browser settings.</p>

<h3>Advertising Opt-Out Tools</h3>
<p>You can opt out of targeted advertising by visiting these resources:</p>
<ul>
    <li><strong>Network Advertising Initiative (NAI):</strong> <a href="http://www.networkadvertising.org/choices/" target="_blank" rel="noopener">NAI Opt-Out Tool</a></li>
    <li><strong>Digital Advertising Alliance (DAA):</strong> <a href="http://www.aboutads.info/choices/" target="_blank" rel="noopener">DAA Opt-Out Tool</a></li>
    <li><strong>European Interactive Digital Advertising Alliance (EDAA):</strong> <a href="http://www.youronlinechoices.eu/" target="_blank" rel="noopener">Your Online Choices</a></li>
    <li><strong>Google Ads Settings:</strong> <a href="https://www.google.com/settings/ads" target="_blank" rel="noopener">Google Ad Personalization</a></li>
    <li><strong>Facebook Ad Preferences:</strong> <a href="https://www.facebook.com/settings?tab=ads" target="_blank" rel="noopener">Facebook Ad Settings</a></li>
</ul>

<h3>Impact of Blocking Cookies</h3>
<p>Please note that if you block or delete cookies:</p>
<ul>
    <li>Some features of our Site may not function properly</li>
    <li>You may not be able to stay logged in</li>
    <li>Your preferences and settings may not be saved</li>
    <li>Shopping cart functionality may be impaired</li>
    <li>You may need to re-enter information on each visit</li>
    <li>We may not be able to customize our Site for you</li>
</ul>

<p>Strictly necessary cookies cannot be disabled through the cookie consent banner, as they are essential for the Site to function. However, you can still block them through your browser settings, though this may prevent you from using certain features of the Site.</p>

<h3>Cookie Consent Management</h3>
<p>You can update your cookie preferences at any time by:</p>
<ul>
    <li>Clicking the cookie preferences link in our website footer</li>
    <li>Clearing your browser cookies and revisiting the Site to see the consent banner again</li>
    <li>Contacting us at {{CONTACT_EMAIL}} to update your preferences</li>
</ul>
