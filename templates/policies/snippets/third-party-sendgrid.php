<h3>SendGrid Email Delivery</h3>

<p>We use SendGrid (operated by Twilio Inc.) as our email delivery service to send transactional emails, account notifications, and other service-related communications.</p>

<p><strong>Information Shared with SendGrid:</strong></p>
<ul>
    <li><strong>Email Address:</strong> Recipient email addresses for delivery purposes</li>
    <li><strong>Email Content:</strong> The content of emails we send through their service</li>
    <li><strong>Delivery Data:</strong> Email delivery status, bounce information, and spam reports</li>
    <li><strong>Engagement Metrics:</strong> Whether emails are opened, links clicked, and when interactions occur</li>
    <li><strong>IP Address:</strong> Your IP address when you interact with emails we send</li>
    <li><strong>Device Information:</strong> Email client, browser type, and operating system used to view emails</li>
</ul>

<p><strong>Purpose of Using SendGrid:</strong></p>
<ul>
    <li><strong>Reliable Delivery:</strong> Ensuring emails reach your inbox promptly and reliably</li>
    <li><strong>Deliverability Management:</strong> Monitoring and maintaining sender reputation to avoid spam filters</li>
    <li><strong>Bounce Handling:</strong> Managing hard bounces, soft bounces, and invalid email addresses</li>
    <li><strong>Engagement Tracking:</strong> Understanding how recipients interact with our emails to improve communications</li>
    <li><strong>Compliance:</strong> Maintaining email sending compliance with CAN-SPAM, GDPR, and other regulations</li>
    <li><strong>Security:</strong> Protecting email content during transmission with encryption and authentication</li>
</ul>

<p><strong>Types of Emails Sent Through SendGrid:</strong></p>
<ul>
    <li>Account creation and verification emails</li>
    <li>Password reset and security notifications</li>
    <li>Order confirmations and shipping updates (if applicable)</li>
    <li>Service notifications and system alerts</li>
    <li>Customer support responses and follow-ups</li>
    <li>Newsletters and marketing emails (if you've opted in)</li>
</ul>

<p><strong>Data Retention:</strong> SendGrid retains email delivery logs and engagement data for a limited period (typically 30-90 days) for deliverability analysis and troubleshooting purposes. Email content itself is not permanently stored by SendGrid once successfully delivered.</p>

<p><strong>Security Measures:</strong></p>
<ul>
    <li><strong>Encryption:</strong> All emails are transmitted using TLS encryption</li>
    <li><strong>Authentication:</strong> SendGrid uses SPF, DKIM, and DMARC email authentication protocols</li>
    <li><strong>Infrastructure Security:</strong> SOC 2 Type II certified secure data centers</li>
    <li><strong>Access Controls:</strong> Strict access controls limiting who can view email data</li>
    <li><strong>Compliance Certifications:</strong> GDPR compliant, Privacy Shield certified (for EU data transfers)</li>
</ul>

<p><strong>Your Rights:</strong></p>
<ul>
    <li><strong>Opt-Out:</strong> You can opt out of non-essential emails using unsubscribe links or by contacting us</li>
    <li><strong>Data Access:</strong> Request information about email engagement data SendGrid has collected about you</li>
    <li><strong>Data Deletion:</strong> Request deletion of your email engagement data from SendGrid's systems</li>
    <li><strong>Correction:</strong> Update your email address to ensure accurate delivery</li>
</ul>

<div class="policy-highlight">
    <p><strong>Data Processing Agreement:</strong> SendGrid processes email data as a data processor on our behalf under a Data Processing Agreement that complies with GDPR Article 28. We remain the data controller responsible for the emails we send. SendGrid's privacy practices are governed by their privacy policy, available at <a href="https://www.twilio.com/legal/privacy" target="_blank" rel="noopener">https://www.twilio.com/legal/privacy</a>.</p>
</div>

<p><strong>Third-Party Data Sharing:</strong> SendGrid does not sell or share your email data with third parties for their own marketing purposes. Data is used solely for email delivery and associated services we have contracted.</p>

<p>If you have concerns about how SendGrid processes your email data or wish to exercise your privacy rights, please contact us using the information in the Contact section of this policy, and we will coordinate with SendGrid on your behalf.</p>
