<h3>POPIA (South Africa) Compliance Statement</h3>
<p>We comply with South Africa's Protection of Personal Information Act 4 of 2013 (POPIA) for processing personal information of data subjects in South Africa.</p>

<h4>Conditions for Lawful Processing</h4>
<p>We process personal information in accordance with POPIA's eight conditions for lawful processing:</p>

<h5>1. Accountability</h5>
<p>We ensure that the conditions set out in POPIA are complied with at the time of determining the purpose and means of processing personal information.</p>

<h5>2. Processing Limitation</h5>
<p>We process personal information lawfully and in a reasonable manner that does not infringe your privacy. We will only process personal information if:</p>
<ul>
    <li>You consent to the processing</li>
    <li>Processing is necessary to carry out actions for the conclusion or performance of a contract</li>
    <li>Processing complies with an obligation imposed by law</li>
    <li>Processing protects a legitimate interest of yours</li>
    <li>Processing is necessary for pursuing our legitimate interests or that of a third party</li>
</ul>

<h5>3. Purpose Specification</h5>
<p>We collect personal information for a specific, explicitly defined, and lawful purpose. We will not use your information for a secondary purpose unless:</p>
<ul>
    <li>You consent to the secondary purpose</li>
    <li>The secondary purpose is directly related to the original purpose</li>
    <li>Processing is necessary to prevent or mitigate a serious and imminent threat</li>
</ul>

<h5>4. Further Processing Limitation</h5>
<p>We will not process personal information for a secondary purpose unless it is compatible with the original purpose for which the information was collected.</p>

<h5>5. Information Quality</h5>
<p>We take reasonably practicable steps to ensure that personal information is complete, accurate, not misleading, and updated where necessary.</p>

<h5>6. Openness</h5>
<p>We take reasonably practicable steps to ensure you are aware of:</p>
<ul>
    <li>The information being collected</li>
    <li>The name and address of the responsible party</li>
    <li>The purpose of collection</li>
    <li>Whether the supply of information is voluntary or mandatory</li>
    <li>The consequences of failure to provide the information</li>
    <li>Your right to access and correct information</li>
    <li>The right to object to the processing of personal information</li>
</ul>

<h5>7. Security Safeguards</h5>
<p>We secure the integrity and confidentiality of personal information in our possession or under our control by taking appropriate, reasonable technical and organizational measures to prevent:</p>
<ul>
    <li>Loss, damage, or unauthorized destruction of personal information</li>
    <li>Unlawful access to or processing of personal information</li>
</ul>

<h5>8. Data Subject Participation</h5>
<p>You have the right to:</p>
<ul>
    <li>Request confirmation of whether we hold personal information about you</li>
    <li>Request access to your personal information</li>
    <li>Request correction, destruction, or deletion of your personal information if it is inaccurate, irrelevant, excessive, out of date, incomplete, misleading, or unlawfully obtained</li>
    <li>Object to the processing of your personal information</li>
    <li>Lodge a complaint with the Information Regulator</li>
</ul>

<h4>Special Personal Information</h4>
<p>We will not process special personal information (race, ethnic origin, political opinions, religious beliefs, health information, biometric information, etc.) without your consent, unless:</p>
<ul>
    <li>Processing is carried out with appropriate safeguards</li>
    <li>Processing is required by law</li>
    <li>Processing is necessary for establishing, exercising, or defending a right or obligation in law</li>
</ul>

<h4>Direct Marketing</h4>
<p>We will not process your personal information for purposes of direct marketing by means of unsolicited electronic communications unless you have given consent or you are an existing customer and certain conditions are met. You have the right to object to direct marketing at any time.</p>

<h4>Information Officer</h4>
<p>We have designated an Information Officer responsible for ensuring compliance with POPIA. You may contact our Information Officer with any questions or concerns about how we handle your personal information.</p>

<h4>Cross-Border Information Transfers</h4>
<p>We may transfer personal information outside South Africa only if:</p>
<ul>
    <li>You consent to the transfer</li>
    <li>The transfer is necessary for the performance of a contract</li>
    <li>The transfer is for your benefit</li>
    <li>The recipient country has substantially similar data protection laws</li>
    <li>Appropriate safeguards are in place</li>
</ul>

<h4>Information Regulator</h4>
<p>You have the right to lodge a complaint with the Information Regulator (South Africa).</p>
