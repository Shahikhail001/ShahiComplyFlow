<h3>Exercising Your Data Subject Rights</h3>
<p>You have the right to make requests regarding your personal data. We are committed to responding to your requests in a timely manner.</p>

<h4>How to Submit a Request</h4>
<p>You can submit a data subject request by:</p>
<ul>
    <li><strong>Email:</strong> Send a request to {{CONTACT_EMAIL}}</li>
    <li><strong>Online Portal:</strong> Use our <a href="/data-subject-request">Data Subject Request Portal</a></li>
    <li><strong>Mail:</strong> Send a written request to {{BUSINESS_ADDRESS}}</li>
</ul>

<h4>Verification Process</h4>
<p>To protect your privacy, we must verify your identity before fulfilling your request. We may ask for:</p>
<ul>
    <li>Email address associated with your account</li>
    <li>Account credentials or security questions</li>
    <li>Government-issued ID (for deletion requests)</li>
</ul>

<h4>Response Time</h4>
<p>We will respond to verifiable requests within:</p>
<ul>
    <li><strong>GDPR:</strong> 1 month (extendable to 3 months for complex requests)</li>
    <li><strong>CCPA:</strong> 45 days (extendable to 90 days)</li>
    <li><strong>LGPD:</strong> 15 days</li>
</ul>

<h4>No Fee</h4>
<p>We do not charge a fee to process your request unless:</p>
<ul>
    <li>The request is clearly unfounded or excessive</li>
    <li>You request multiple copies of the same information</li>
</ul>

<h4>Authorized Agents</h4>
<p>You may designate an authorized agent to submit requests on your behalf. The agent must provide proof of authorization (e.g., power of attorney).</p>

<h4>Appeal Process</h4>
<p>If you are not satisfied with our response, you have the right to:</p>
<ul>
    <li>Appeal our decision</li>
    <li>Lodge a complaint with the relevant data protection authority</li>
    <li>Seek judicial remedy</li>
</ul>
