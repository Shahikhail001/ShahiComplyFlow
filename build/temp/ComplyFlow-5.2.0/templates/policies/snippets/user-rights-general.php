<h3>Your Rights - General</h3>
<p>You have certain rights regarding your personal information:</p>
<ul>
    <li><strong>Access:</strong> You can request a copy of the personal information we hold about you</li>
    <li><strong>Correction:</strong> You can request that we correct inaccurate or incomplete information</li>
    <li><strong>Deletion:</strong> You can request that we delete your personal information</li>
    <li><strong>Object:</strong> You can object to our processing of your personal information</li>
    <li><strong>Restrict:</strong> You can request that we restrict the processing of your personal information</li>
    <li><strong>Data Portability:</strong> You can request a copy of your data in a machine-readable format</li>
</ul>

<p>To exercise these rights, please contact us using the contact information provided at the end of this policy.</p>
