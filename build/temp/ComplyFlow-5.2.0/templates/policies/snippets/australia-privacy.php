<h2>Australian Privacy Act Compliance</h2>

<p>This section applies to individuals whose personal information is collected in Australia or who are Australian residents. We are committed to complying with the Privacy Act 1988 (Cth) and the Australian Privacy Principles (APPs).</p>

<h3>Collection of Personal Information</h3>
<p>We collect personal information in accordance with APP 3 (Collection of solicited personal information). We only collect information that is reasonably necessary for our functions and activities. We collect personal information directly from you where practicable, and we will notify you about:</p>
<ul>
    <li>Our identity and how to contact us</li>
    <li>The purposes for which we collect, hold, use, and disclose your personal information</li>
    <li>The consequences if we do not collect the information</li>
    <li>Whether we are likely to disclose your information to overseas recipients</li>
    <li>How you can access and seek correction of your information</li>
    <li>How you can make a complaint about a breach of the APPs</li>
</ul>

<h3>Use and Disclosure of Personal Information</h3>
<p>In accordance with APP 6 (Use or disclosure of personal information), we will only use or disclose your personal information:</p>
<ul>
    <li>For the primary purpose for which it was collected</li>
    <li>For a secondary purpose that is related to the primary purpose and you would reasonably expect such use or disclosure</li>
    <li>With your consent</li>
    <li>Where required or authorized by law</li>
    <li>To lessen or prevent a serious threat to life, health, or safety</li>
</ul>

<h3>Direct Marketing</h3>
<p>In accordance with APP 7 (Direct marketing), we may use your personal information for direct marketing purposes only if:</p>
<ul>
    <li>We collected the information directly from you</li>
    <li>You would reasonably expect us to use or disclose the information for direct marketing</li>
    <li>We provide a simple way to opt-out of direct marketing</li>
    <li>You have not requested to opt-out</li>
</ul>
<p>You have the right to request that we do not use or disclose your personal information for direct marketing purposes. We will honor such requests promptly and free of charge.</p>

<h3>Cross-Border Disclosure</h3>
<p>In accordance with APP 8 (Cross-border disclosure of personal information), if we disclose your personal information to overseas recipients, we take reasonable steps to ensure that overseas recipients do not breach the APPs in relation to your information. We may disclose personal information to recipients located in:</p>
<ul>
    <li>United States (for cloud hosting and service providers)</li>
    <li>European Union (for analytics and marketing services)</li>
    <li>Other countries where our service providers operate</li>
</ul>
<p>We ensure that overseas transfers are covered by appropriate safeguards, such as:</p>
<ul>
    <li>Contractual arrangements that require recipients to protect your information in accordance with Australian privacy standards</li>
    <li>Transfers to countries with substantially similar privacy protections</li>
    <li>Your express consent to the overseas disclosure</li>
</ul>

<h3>Data Quality and Security</h3>
<p>In accordance with APP 10 (Quality of personal information) and APP 11 (Security of personal information):</p>
<ul>
    <li><strong>Data Quality:</strong> We take reasonable steps to ensure that the personal information we collect, use, and disclose is accurate, up-to-date, complete, and relevant</li>
    <li><strong>Data Security:</strong> We take reasonable steps to protect personal information from misuse, interference, loss, unauthorized access, modification, or disclosure. This includes:
        <ul>
            <li>Secure storage systems with access controls</li>
            <li>Encryption of data in transit and at rest</li>
            <li>Regular security assessments and audits</li>
            <li>Staff training on privacy and security obligations</li>
            <li>Secure destruction or de-identification of information no longer needed</li>
        </ul>
    </li>
</ul>

<h3>Your Rights Under the Australian Privacy Act</h3>

<h4>Access to Personal Information (APP 12)</h4>
<p>You have the right to request access to the personal information we hold about you. Upon receiving a valid request, we will provide you with access within a reasonable period (usually 30 days). We may charge a reasonable fee for providing access, but we will inform you of any charges before processing your request.</p>
<p>We may refuse access in certain circumstances permitted by the Privacy Act, such as:</p>
<ul>
    <li>Providing access would pose a serious threat to life, health, or safety</li>
    <li>Access would have an unreasonable impact on the privacy of others</li>
    <li>The request is frivolous or vexatious</li>
    <li>The information relates to existing or anticipated legal proceedings</li>
    <li>Access would be unlawful or would prejudice enforcement activities</li>
</ul>
<p>If we refuse your request, we will provide written reasons for the refusal and inform you of the mechanisms available to complain about the refusal.</p>

<h4>Correction of Personal Information (APP 13)</h4>
<p>You have the right to request correction of personal information we hold about you if you believe it is inaccurate, out-of-date, incomplete, irrelevant, or misleading. We will respond to your correction request within a reasonable period (usually 30 days). If we refuse to correct your information, we will:</p>
<ul>
    <li>Provide written reasons for the refusal</li>
    <li>Include a statement with your information noting that you consider it inaccurate, out-of-date, incomplete, irrelevant, or misleading</li>
    <li>Take reasonable steps to provide the statement to any third parties to whom we have disclosed the information</li>
</ul>

<h4>Making a Complaint</h4>
<p>If you believe we have breached the Australian Privacy Principles, you have the right to make a complaint. To lodge a complaint:</p>
<ol>
    <li><strong>Contact Us:</strong> Send your complaint in writing to the contact details provided in the Contact section of this policy. Include:
        <ul>
            <li>Your name and contact details</li>
            <li>Details of the alleged breach</li>
            <li>Any relevant dates, locations, or communications</li>
            <li>Your desired outcome</li>
        </ul>
    </li>
    <li><strong>Our Response:</strong> We will acknowledge your complaint within 7 days and investigate the matter</li>
    <li><strong>Resolution:</strong> We will provide a written response within 30 days, including:
        <ul>
            <li>Our findings and decision</li>
            <li>Reasons for our decision</li>
            <li>Any action we will take to address the issue</li>
            <li>Information about your right to complain to the OAIC if you are dissatisfied</li>
        </ul>
    </li>
    <li><strong>Escalation to OAIC:</strong> If you are not satisfied with our response or if we fail to respond within 30 days, you may lodge a complaint with the Office of the Australian Information Commissioner (OAIC):
        <ul>
            <li><strong>Website:</strong> <a href="https://www.oaic.gov.au/" target="_blank" rel="noopener">www.oaic.gov.au</a></li>
            <li><strong>Email:</strong> enquiries@oaic.gov.au</li>
            <li><strong>Phone:</strong> 1300 363 992</li>
            <li><strong>Mail:</strong> GPO Box 5218, Sydney NSW 2001</li>
        </ul>
    </li>
</ol>

<h3>Anonymity and Pseudonymity (APP 2)</h3>
<p>Where practicable, we will give you the option to interact with us anonymously or using a pseudonym. However, in many cases, we will need to collect personal information to provide our services effectively. If you choose not to provide requested information, we may not be able to provide certain services to you.</p>

<h3>Sensitive Information</h3>
<p>We do not generally collect sensitive information (such as information about your health, racial or ethnic origin, political opinions, religious beliefs, sexual orientation, or criminal record) unless:</p>
<ul>
    <li>You have provided express consent</li>
    <li>Collection is required or authorized by law</li>
    <li>Collection is necessary to prevent or lessen a serious threat to life, health, or safety</li>
</ul>

