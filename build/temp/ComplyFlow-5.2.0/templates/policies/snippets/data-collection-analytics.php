<h3>Analytics Data Collection</h3>
<p>We use analytics services (such as Google Analytics) to understand how visitors use our website. These services collect:</p>
<ul>
    <li>Pages visited and time spent on each page</li>
    <li>Referral sources (how you found our website)</li>
    <li>Device and browser information</li>
    <li>Demographic information (age range, gender, interests)</li>
</ul>
<p>This information is aggregated and anonymized. It helps us improve our website and provide better content.</p>
