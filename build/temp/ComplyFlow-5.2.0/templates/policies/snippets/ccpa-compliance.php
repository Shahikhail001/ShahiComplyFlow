<h3>CCPA Compliance Statement</h3>
<p>We comply with the California Consumer Privacy Act (CCPA) for California residents.</p>

<h4>Categories of Personal Information</h4>
<p>We collect the following categories of personal information:</p>
<ul>
    <li><strong>Identifiers:</strong> Name, email address, IP address</li>
    <li><strong>Commercial Information:</strong> Purchase history, browsing history</li>
    <li><strong>Internet Activity:</strong> Website interactions, search history</li>
    <li><strong>Geolocation Data:</strong> General location based on IP address</li>
    <li><strong>Inferences:</strong> Preferences and characteristics derived from your activity</li>
</ul>

<h4>Sale of Personal Information</h4>
<p><strong>Do Not Sell My Personal Information:</strong> We do not sell your personal information to third parties. We may share information with service providers and business partners as described in our Privacy Policy.</p>

<h4>Authorized Agent</h4>
<p>You may designate an authorized agent to make CCPA requests on your behalf. The agent must provide proof of authorization.</p>

<h4>Non-Discrimination</h4>
<p>We will not discriminate against you for exercising your CCPA rights. This means we will not:</p>
<ul>
    <li>Deny goods or services</li>
    <li>Charge different prices or rates</li>
    <li>Provide different quality of goods or services</li>
    <li>Suggest you will receive different prices or quality</li>
</ul>

<h4>Verification Process</h4>
<p>To verify CCPA requests, we may ask for:</p>
<ul>
    <li>Email address and name</li>
    <li>Account information (if applicable)</li>
    <li>Additional information to match our records</li>
</ul>
