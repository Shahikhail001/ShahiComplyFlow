<h3>Data Protection Officer</h3>

<p>We have appointed a Data Protection Officer (DPO) who is responsible for overseeing our data protection strategy and ensuring compliance with applicable data protection laws.</p>

<p><strong>You can contact our DPO for:</strong></p>
<ul>
    <li>Questions about how we process your personal data</li>
    <li>Requests to exercise your data protection rights</li>
    <li>Concerns about our data protection practices</li>
    <li>Complaints about data privacy matters</li>
</ul>

<p>Our DPO can be reached through the contact information provided in this policy. We will respond to all DPO inquiries within the timeframes required by applicable law.</p>
