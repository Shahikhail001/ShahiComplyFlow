<h3>Mailchimp Email Marketing</h3>

<p>We use Mailchimp to manage our email marketing campaigns and newsletters. Mailchimp is a marketing automation platform operated by Intuit Inc.</p>

<p><strong>Information Shared with Mailchimp:</strong></p>
<ul>
    <li><strong>Email Address:</strong> Your email address when you subscribe to our mailing list</li>
    <li><strong>Name:</strong> If you provide your name during subscription</li>
    <li><strong>Subscription Preferences:</strong> Your communication preferences and interests</li>
    <li><strong>Engagement Data:</strong> Whether you open emails, click links, or interact with our campaigns</li>
    <li><strong>Device Information:</strong> Device type, email client, and IP address when you interact with our emails</li>
</ul>

<p><strong>How This Information Is Used:</strong></p>
<ul>
    <li>Sending newsletters and marketing communications you've subscribed to</li>
    <li>Personalizing email content based on your interests and preferences</li>
    <li>Analyzing campaign performance to improve our communications</li>
    <li>Managing subscription lists and unsubscribe requests</li>
    <li>Ensuring compliance with anti-spam laws (CAN-SPAM, GDPR)</li>
</ul>

<p><strong>Double Opt-In:</strong> We use Mailchimp's double opt-in feature, which means you must confirm your subscription by clicking a link in a confirmation email. This ensures that only people who genuinely want to receive our emails are added to our list.</p>

<p><strong>Your Control Over Mailchimp Data:</strong></p>
<ul>
    <li><strong>Unsubscribe:</strong> Every email includes an unsubscribe link that immediately removes you from our mailing list</li>
    <li><strong>Update Preferences:</strong> You can update your email preferences or subscription settings at any time</li>
    <li><strong>Data Deletion:</strong> You can request deletion of your data from Mailchimp through our contact information</li>
</ul>

<p><strong>Data Security:</strong> Mailchimp employs industry-standard security measures including encryption, secure data centers, and regular security audits. They are certified under the EU-U.S. Privacy Framework for GDPR compliance.</p>

<p><strong>Data Retention:</strong> Mailchimp retains your data as long as you remain subscribed to our mailing list. After unsubscribing, your data is retained for a limited period as required by law or for legitimate business purposes (such as maintaining unsubscribe records to honor your opt-out preference).</p>

