<h3>Cookies Overview</h3>
<p>We use cookies and similar tracking technologies to track activity on our website and hold certain information. Cookies are files with small amounts of data that are sent to your browser from a website and stored on your device.</p>

<p><strong>Types of cookies we use:</strong></p>
<ul>
    <li><strong>Necessary Cookies:</strong> Required for the website to function properly</li>
    <li><strong>Analytics Cookies:</strong> Help us understand how you use our website</li>
    <li><strong>Marketing Cookies:</strong> Used to deliver relevant advertisements</li>
    <li><strong>Preference Cookies:</strong> Remember your settings and preferences</li>
</ul>

<p>You can control cookie settings through your browser. However, disabling cookies may affect your ability to use certain features of our website.</p>

