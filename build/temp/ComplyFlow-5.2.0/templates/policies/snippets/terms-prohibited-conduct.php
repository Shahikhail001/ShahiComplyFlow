<h2>Prohibited Conduct</h2>

<p>You agree not to engage in any of the following prohibited activities in connection with your use of the Site:</p>

<h3>Prohibited Uses</h3>
<ul>
    <li><strong>Illegal Activities:</strong> Using the Site for any illegal purpose or in violation of any local, state, national, or international law</li>
    <li><strong>Fraud:</strong> Engaging in any fraudulent activity, including impersonating any person or entity or falsely stating or misrepresenting your affiliation with any person or entity</li>
    <li><strong>Unauthorized Access:</strong> Attempting to gain unauthorized access to any portion of the Site, other user accounts, or any computer systems or networks connected to the Site</li>
    <li><strong>Interference:</strong> Interfering with or disrupting the Site, servers, or networks connected to the Site, or disobeying any requirements, procedures, policies, or regulations of networks connected to the Site</li>
    <li><strong>Security Violations:</strong> Violating the security of the Site, including:
        <ul>
            <li>Accessing data not intended for you or logging into a server or account you are not authorized to access</li>
            <li>Attempting to probe, scan, or test the vulnerability of a system or network or to breach security or authentication measures</li>
            <li>Interfering with service to any user, host, or network, including by means of submitting a virus, overloading, flooding, spamming, or mail-bombing</li>
            <li>Forging any TCP/IP packet header or any part of the header information in any email or posting</li>
        </ul>
    </li>
</ul>

<h3>Prohibited Data Collection</h3>
<ul>
    <li><strong>Scraping:</strong> Using any robot, spider, scraper, or other automated means to access the Site for any purpose without our express written permission</li>
    <li><strong>Harvesting:</strong> Harvesting or collecting email addresses or other contact information of users from the Site by electronic or other means</li>
    <li><strong>Data Mining:</strong> Using any data mining, robots, or similar data gathering or extraction methods</li>
    <li><strong>Framing:</strong> Framing or linking to the Site without permission</li>
</ul>

<h3>Prohibited Commercial Activities</h3>
<ul>
    <li><strong>Unauthorized Sales:</strong> Using the Site to sell or promote products or services without our permission</li>
    <li><strong>Spam:</strong> Sending unsolicited or unauthorized advertising, promotional materials, spam, junk mail, chain letters, or any other form of solicitation through or using the Site</li>
    <li><strong>Commercial Use:</strong> Using the Site for any commercial purpose not expressly approved by us</li>
    <li><strong>Competitive Use:</strong> Using information obtained from the Site to compete with us or for the benefit of a competing business</li>
</ul>

<h3>Prohibited Content Distribution</h3>
<ul>
    <li><strong>Malware:</strong> Uploading, posting, or otherwise transmitting any content that contains software viruses or any other computer code, files, or programs designed to interrupt, destroy, or limit the functionality of any computer software, hardware, or telecommunications equipment</li>
    <li><strong>Harmful Content:</strong> Posting content that is harmful, threatening, abusive, harassing, defamatory, vulgar, obscene, libelous, or otherwise objectionable</li>
    <li><strong>Infringing Content:</strong> Posting content that infringes any patent, trademark, trade secret, copyright, or other proprietary rights of any party</li>
    <li><strong>Private Information:</strong> Posting another person's private or personal information without their consent</li>
</ul>

<h3>Account Abuse</h3>
<ul>
    <li><strong>Multiple Accounts:</strong> Creating multiple accounts to evade restrictions or abuse the Site</li>
    <li><strong>False Information:</strong> Providing false, inaccurate, or misleading information when creating an account or using the Site</li>
    <li><strong>Account Sharing:</strong> Sharing your account credentials with others or allowing others to access your account</li>
    <li><strong>Circumvention:</strong> Attempting to circumvent any content filtering, security measures, or access controls</li>
</ul>

<h3>Manipulation and Abuse</h3>
<ul>
    <li><strong>Rating Manipulation:</strong> Artificially inflating or deflating ratings, reviews, likes, or other metrics</li>
    <li><strong>Fake Reviews:</strong> Posting fake, misleading, or biased reviews</li>
    <li><strong>Vote Brigading:</strong> Organizing coordinated efforts to manipulate voting, ratings, or rankings</li>
    <li><strong>Astroturfing:</strong> Creating false grassroots campaigns or fake support</li>
</ul>

<h3>Consequences of Prohibited Conduct</h3>
<p>If you engage in any prohibited conduct, we may:</p>
<ul>
    <li>Terminate or suspend your account immediately</li>
    <li>Remove any content you have posted</li>
    <li>Report your activities to law enforcement authorities</li>
    <li>Take legal action against you, including seeking monetary damages and injunctive relief</li>
    <li>Ban your IP address or device from accessing the Site</li>
</ul>

