<h3>Returns, Refunds, and Cancellations</h3>

<p>We want you to be satisfied with your purchase. If you are not completely satisfied, we offer the following return and refund policy:</p>

<h4>Return Eligibility</h4>
<p>To be eligible for a return, items must meet the following conditions:</p>
<ul>
    <li><strong>Timeframe:</strong> Return requests must be initiated within 30 days of delivery</li>
    <li><strong>Condition:</strong> Items must be unused, in original condition, and in original packaging with all tags attached</li>
    <li><strong>Proof of Purchase:</strong> You must provide proof of purchase (order number or receipt)</li>
    <li><strong>Non-Returnable Items:</strong> Some items cannot be returned, including:
        <ul>
            <li>Digital products and downloadable software</li>
            <li>Custom or personalized items</li>
            <li>Perishable goods</li>
            <li>Intimate or sanitary goods</li>
            <li>Items marked as final sale or clearance</li>
        </ul>
    </li>
</ul>

<h4>Return Process</h4>
<ol>
    <li><strong>Request Authorization:</strong> Contact us at {{CONTACT_EMAIL}} to request a Return Merchandise Authorization (RMA) number. Do not return items without an RMA number</li>
    <li><strong>Package Items:</strong> Securely package the items in their original packaging if possible</li>
    <li><strong>Include RMA Number:</strong> Clearly write the RMA number on the outside of the package</li>
    <li><strong>Ship Items:</strong> Ship the package to the return address provided. You are responsible for return shipping costs unless the return is due to our error</li>
    <li><strong>Tracking Recommended:</strong> We recommend using a trackable shipping method and purchasing shipping insurance, as we are not responsible for return packages that are lost or damaged in transit</li>
</ol>

<h4>Refund Policy</h4>
<ul>
    <li><strong>Inspection:</strong> Once we receive your return, we will inspect the items to ensure they meet return eligibility requirements</li>
    <li><strong>Processing Time:</strong> Refunds will be processed within 5-10 business days of receiving your return</li>
    <li><strong>Refund Method:</strong> Refunds will be issued to the original payment method used for the purchase</li>
    <li><strong>Refund Amount:</strong> The refund amount will include the purchase price of the returned items. Original shipping charges are non-refundable unless the return is due to our error</li>
    <li><strong>Restocking Fee:</strong> A restocking fee of up to 15% may be applied to returns, particularly for opened or large items</li>
    <li><strong>Partial Refunds:</strong> Partial refunds may be granted for items that are not in original condition, damaged, or missing parts for reasons not due to our error</li>
</ul>

<h4>Exchanges</h4>
<ul>
    <li><strong>Availability:</strong> We do not offer direct exchanges. If you wish to exchange an item, please return it for a refund and place a new order</li>
    <li><strong>Defective Items:</strong> If you receive a defective or incorrect item, we will replace it at no additional cost or provide a full refund including shipping</li>
</ul>

<h4>Order Cancellations</h4>
<ul>
    <li><strong>Before Shipment:</strong> Orders can be canceled before they ship. Contact us immediately at {{CONTACT_EMAIL}} to cancel your order. You will receive a full refund</li>
    <li><strong>After Shipment:</strong> Once an order has shipped, it cannot be canceled. You must wait to receive the order and then initiate a return</li>
    <li><strong>Processing Status:</strong> Orders in the processing stage may or may not be cancelable depending on how far along they are. Contact us as soon as possible if you wish to cancel</li>
</ul>

<h4>Digital Products and Services</h4>
<p>For digital products, downloadable content, and services:</p>
<ul>
    <li><strong>No Refunds:</strong> Generally, digital products and services are non-refundable once access has been granted or download has begun</li>
    <li><strong>Exceptions:</strong> Refunds may be provided if:
        <ul>
            <li>The digital product is defective and cannot be fixed</li>
            <li>You were charged incorrectly</li>
            <li>Access was never granted due to technical issues on our end</li>
        </ul>
    </li>
    <li><strong>Request Timeframe:</strong> Refund requests for digital products must be made within 14 days of purchase</li>
</ul>

