<h2>GDPR Compliance (European Union)</h2>
<p>If you are located in the European Economic Area (EEA), you have specific rights under the General Data Protection Regulation (GDPR):</p>

<h3>Your GDPR Rights</h3>
<ul>
    <li><strong>Right to Access:</strong> Request a copy of the personal data we hold about you</li>
    <li><strong>Right to Rectification:</strong> Request correction of inaccurate or incomplete data</li>
    <li><strong>Right to Erasure ("Right to be Forgotten"):</strong> Request deletion of your personal data</li>
    <li><strong>Right to Restrict Processing:</strong> Request that we limit how we use your data</li>
    <li><strong>Right to Data Portability:</strong> Receive your data in a structured, machine-readable format</li>
    <li><strong>Right to Object:</strong> Object to processing of your data for certain purposes</li>
    <li><strong>Right to Withdraw Consent:</strong> Withdraw consent at any time (where processing is based on consent)</li>
    <li><strong>Right to Lodge a Complaint:</strong> File a complaint with your local data protection authority</li>
</ul>

<h3>Legal Basis for Processing</h3>
<p>We process your personal data based on one or more of the following legal grounds:</p>
<ul>
    <li><strong>Consent:</strong> You have given explicit consent for specific processing activities</li>
    <li><strong>Contract Performance:</strong> Processing is necessary to fulfill our contract with you</li>
    <li><strong>Legal Obligation:</strong> We must process your data to comply with the law</li>
    <li><strong>Legitimate Interests:</strong> Processing is necessary for our legitimate business interests</li>
</ul>

<h3>International Data Transfers</h3>
<p>We may transfer your data outside the EEA. When we do, we ensure appropriate safeguards are in place, such as:</p>
<ul>
    <li>EU-US Data Privacy Framework certification</li>
    <li>Standard Contractual Clauses approved by the European Commission</li>
    <li>Adequacy decisions for countries with appropriate data protection levels</li>
</ul>

