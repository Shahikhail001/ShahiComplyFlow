<h2>CCPA Compliance (California)</h2>
<p>If you are a California resident, you have specific rights under the California Consumer Privacy Act (CCPA) and California Privacy Rights Act (CPRA):</p>

<h3>Your California Privacy Rights</h3>
<ul>
    <li><strong>Right to Know:</strong> Request information about the categories and specific pieces of personal information we have collected</li>
    <li><strong>Right to Delete:</strong> Request deletion of your personal information</li>
    <li><strong>Right to Opt-Out:</strong> Opt out of the "sale" or "sharing" of your personal information</li>
    <li><strong>Right to Correct:</strong> Request correction of inaccurate personal information</li>
    <li><strong>Right to Limit:</strong> Limit the use and disclosure of sensitive personal information</li>
    <li><strong>Right to Non-Discrimination:</strong> Exercise your privacy rights without discriminatory treatment</li>
</ul>

<h3>Categories of Personal Information Collected</h3>
<p>We collect the following categories of personal information:</p>
<ul>
    <li>Identifiers (name, email, IP address)</li>
    <li>Commercial information (purchase history, browsing history)</li>
    <li>Internet activity (browsing behavior, interactions with our Site)</li>
    <li>Geolocation data (approximate location based on IP address)</li>
    <li>Professional information (if provided in business context)</li>
</ul>

<h3>Do Not Sell My Personal Information</h3>
<p>We do not "sell" personal information as defined by CCPA. However, we may share information with third-party service providers for business purposes.</p>

<h3>How to Exercise Your Rights</h3>
<p>To exercise your CCPA rights:</p>
<ul>
    <li>Email us at {{CONTACT_EMAIL}}</li>
    <li>We will verify your identity before processing your request</li>
    <li>We will respond within 45 days of receiving your request</li>
    <li>You may designate an authorized agent to make requests on your behalf</li>
</ul>

<h3>Shine the Light Law</h3>
<p>California's "Shine the Light" law permits California residents to request information about our disclosure of personal information to third parties for direct marketing purposes. We do not share personal information with third parties for their direct marketing purposes.</p>
