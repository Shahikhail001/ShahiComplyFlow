<h3>GDPR Rights (European Union)</h3>
<p>If you are a resident of the European Economic Area (EEA), you have additional rights under the General Data Protection Regulation (GDPR):</p>
<ul>
    <li><strong>Right to Access:</strong> You have the right to know what personal data we process about you</li>
    <li><strong>Right to Rectification:</strong> You can correct inaccurate data</li>
    <li><strong>Right to Erasure ("Right to be Forgotten"):</strong> You can request deletion of your data</li>
    <li><strong>Right to Restriction:</strong> You can limit how we use your data</li>
    <li><strong>Right to Data Portability:</strong> You can receive your data in a portable format</li>
    <li><strong>Right to Object:</strong> You can object to our processing, especially for marketing purposes</li>
    <li><strong>Right to Withdraw Consent:</strong> You can withdraw consent at any time</li>
    <li><strong>Right to Lodge a Complaint:</strong> You can file a complaint with your local data protection authority</li>
</ul>

<p>We process your personal data based on one or more of the following legal bases:</p>
<ul>
    <li>Your explicit consent</li>
    <li>Performance of a contract with you</li>
    <li>Compliance with a legal obligation</li>
    <li>Our legitimate interests (which do not override your rights)</li>
</ul>
