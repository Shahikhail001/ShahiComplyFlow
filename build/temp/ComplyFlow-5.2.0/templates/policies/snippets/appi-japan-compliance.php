<h3>APPI (Japan) Compliance Statement</h3>
<p>We comply with Japan's Act on the Protection of Personal Information (APPI) for processing personal information of individuals in Japan.</p>

<h4>Appropriate Acquisition of Personal Information</h4>
<p>We acquire personal information by appropriate means and will not:</p>
<ul>
    <li>Acquire personal information by deception or other wrongful means</li>
    <li>Acquire special care-required personal information without your consent, except where permitted by law</li>
</ul>

<h4>Purpose of Use</h4>
<p>We will:</p>
<ul>
    <li>Specify the purpose of use of personal information as clearly as possible</li>
    <li>Publicly announce or notify you of the purpose of use when acquiring personal information</li>
    <li>Not handle personal information beyond the scope necessary to achieve the specified purpose of use</li>
    <li>Obtain your consent if we wish to change the purpose of use beyond what is reasonably deemed relevant</li>
</ul>

<h4>Special Care-Required Personal Information</h4>
<p>For sensitive personal information (race, creed, social status, medical history, criminal record, etc.), we will not acquire, use, or provide such information without your consent, except where permitted by law.</p>

<h4>Security Management Measures</h4>
<p>We take necessary and appropriate measures for the security management of personal data, including:</p>
<ul>
    <li>Preventing leakage, loss, or damage</li>
    <li>Ensuring proper management of personal data</li>
    <li>Supervising employees who handle personal data</li>
    <li>Supervising contractors to whom we entrust personal data</li>
</ul>

<h4>Provision to Third Parties</h4>
<p>We will not provide personal data to third parties without your consent, except where:</p>
<ul>
    <li>Required by law</li>
    <li>Necessary for the protection of human life, body, or property</li>
    <li>Necessary for improving public health or promoting sound development of children</li>
    <li>Necessary for cooperating with national or local government agencies</li>
</ul>

<h4>Cross-Border Transfer</h4>
<p>When providing personal data to a third party in a foreign country, we will:</p>
<ul>
    <li>Obtain your consent</li>
    <li>Ensure the foreign country has a system equivalent to Japan's APPI, or</li>
    <li>Ensure the third party establishes a system equivalent to the standards in Japan</li>
</ul>

<h4>Your Rights Under APPI</h4>
<p>You have the right to:</p>
<ul>
    <li><strong>Disclosure:</strong> Request disclosure of your personal data</li>
    <li><strong>Correction:</strong> Request correction of inaccurate personal data</li>
    <li><strong>Suspension of Use:</strong> Request suspension of use or erasure of personal data if it was acquired or used in violation of APPI</li>
    <li><strong>Suspension of Provision:</strong> Request suspension of provision to third parties if done in violation of APPI</li>
</ul>

<h4>Retention Personal Data</h4>
<p>For personal data we retain, we will:</p>
<ul>
    <li>Publicly announce or make readily accessible the following matters:
        <ul>
            <li>Our name and address</li>
            <li>Purpose of use of retained personal data</li>
            <li>Procedures for responding to requests for disclosure, correction, suspension of use, etc.</li>
            <li>Contact point for complaints</li>
        </ul>
    </li>
    <li>Endeavor to delete personal data without delay when it becomes unnecessary</li>
</ul>

<h4>Anonymized Information</h4>
<p>When we create anonymized information (personal information processed so individuals cannot be identified), we will:</p>
<ul>
    <li>Take measures to prevent re-identification</li>
    <li>Publicly announce items of information related to the anonymized information and the purpose of use</li>
    <li>Not provide anonymized information to third parties without taking certain measures</li>
</ul>

