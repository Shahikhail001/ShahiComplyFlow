<h3>Data Security</h3>
<p>We implement appropriate technical and organizational security measures to protect your personal information against unauthorized access, alteration, disclosure, or destruction. These measures include:</p>

<ul>
    <li><strong>Encryption:</strong> Data transmitted over the internet is encrypted using SSL/TLS protocols</li>
    <li><strong>Access Controls:</strong> Limited access to personal data on a need-to-know basis</li>
    <li><strong>Regular Security Audits:</strong> Periodic assessment of our security practices</li>
    <li><strong>Employee Training:</strong> Staff training on data protection and security best practices</li>
    <li><strong>Secure Infrastructure:</strong> Use of secure servers and databases with regular backups</li>
</ul>

<p>However, no method of transmission over the internet or electronic storage is 100% secure. While we strive to use commercially acceptable means to protect your personal data, we cannot guarantee absolute security.</p>

<div class="highlight">
    <p><strong>Data Breach Notification:</strong> In the event of a data breach that affects your personal information, we will notify you and relevant authorities in accordance with applicable laws.</p>
</div>
