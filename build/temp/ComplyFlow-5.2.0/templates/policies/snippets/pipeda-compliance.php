<h3>PIPEDA Compliance Statement</h3>
<p>We comply with Canada's Personal Information Protection and Electronic Documents Act (PIPEDA) for processing personal information of Canadian residents.</p>

<h4>Fair Information Principles</h4>
<p>We adhere to PIPEDA's 10 Fair Information Principles:</p>
<ol>
    <li><strong>Accountability:</strong> We are responsible for personal information under our control and have designated individuals accountable for compliance</li>
    <li><strong>Identifying Purposes:</strong> We identify the purposes for which personal information is collected at or before the time of collection</li>
    <li><strong>Consent:</strong> We obtain your knowledge and consent for the collection, use, or disclosure of personal information, except where inappropriate</li>
    <li><strong>Limiting Collection:</strong> We limit the collection of personal information to that which is necessary for identified purposes</li>
    <li><strong>Limiting Use, Disclosure, and Retention:</strong> We do not use or disclose personal information for purposes other than those for which it was collected, except with your consent or as required by law</li>
    <li><strong>Accuracy:</strong> We keep personal information as accurate, complete, and up-to-date as necessary</li>
    <li><strong>Safeguards:</strong> We protect personal information with security safeguards appropriate to the sensitivity of the information</li>
    <li><strong>Openness:</strong> We make information about our policies and practices relating to personal information readily available</li>
    <li><strong>Individual Access:</strong> Upon request, we inform you of the existence, use, and disclosure of your personal information</li>
    <li><strong>Challenging Compliance:</strong> You can challenge our compliance with these principles</li>
</ol>

<h4>Meaningful Consent</h4>
<p>We ensure consent is meaningful by:</p>
<ul>
    <li>Using clear and simple language</li>
    <li>Making consent forms easy to read and understand</li>
    <li>Separating consent requests for different purposes</li>
    <li>Explaining the consequences of consenting or refusing to consent</li>
    <li>Allowing you to withdraw consent easily</li>
</ul>

<h4>Your Rights Under PIPEDA</h4>
<p>You have the right to:</p>
<ul>
    <li>Access your personal information held by us</li>
    <li>Request correction of inaccurate or incomplete information</li>
    <li>Withdraw consent for the use of your personal information</li>
    <li>File a complaint with the Office of the Privacy Commissioner of Canada</li>
</ul>

<h4>Breach Notification</h4>
<p>In the event of a breach of security safeguards involving personal information that poses a real risk of significant harm, we will:</p>
<ul>
    <li>Notify affected individuals</li>
    <li>Notify the Privacy Commissioner of Canada</li>
    <li>Maintain records of all breaches</li>
</ul>

