<h2>Cookie Categories</h2>

<p>Cookies can be categorized based on their purpose. Below are the different categories of cookies that may be used on websites:</p>

<h3>Strictly Necessary Cookies</h3>
<p>These cookies are essential for the website to function properly. They enable core functionality such as security, network management, and accessibility. Without these cookies, the website cannot operate correctly, and they cannot be disabled.</p>

<p><strong>Purpose:</strong></p>
<ul>
    <li>Authentication and login functionality</li>
    <li>Shopping cart and checkout processes</li>
    <li>Security and fraud prevention</li>
    <li>Load balancing and performance optimization</li>
    <li>Remembering your cookie consent preferences</li>
</ul>

<h3>Functional Cookies</h3>
<p>These cookies enable enhanced functionality and personalization. They may be set by us or by third-party providers whose services we use on our pages. If you do not allow these cookies, some or all of these services may not function properly.</p>

<p><strong>Purpose:</strong></p>
<ul>
    <li>Remembering your preferences (language, region, currency)</li>
    <li>Personalizing content based on your interests</li>
    <li>Providing live chat support</li>
    <li>Remembering items in your wishlist</li>
    <li>Video playback and embedded content</li>
</ul>

<h3>Performance/Analytics Cookies</h3>
<p>These cookies allow us to count visits and traffic sources so we can measure and improve the performance of our Site. They help us understand which pages are the most and least popular and see how visitors move around the Site. All information collected by these cookies is aggregated and therefore anonymous.</p>

<p><strong>Purpose:</strong></p>
<ul>
    <li>Analyzing website traffic and user behavior</li>
    <li>Identifying popular content and navigation paths</li>
    <li>Detecting and fixing technical issues</li>
    <li>A/B testing and website optimization</li>
    <li>Measuring campaign effectiveness</li>
</ul>

<h3>Targeting/Advertising Cookies</h3>
<p>These cookies are used to deliver advertisements that are relevant to you and your interests. They are also used to limit the number of times you see an advertisement and to measure the effectiveness of advertising campaigns. These cookies may be set by our advertising partners through our Site.</p>

<p><strong>Purpose:</strong></p>
<ul>
    <li>Displaying personalized advertisements</li>
    <li>Tracking ad performance and conversions</li>
    <li>Building audience profiles for targeted advertising</li>
    <li>Retargeting visitors who have previously visited the Site</li>
    <li>Limiting ad frequency (frequency capping)</li>
</ul>

<h3>Social Media Cookies</h3>
<p>These cookies are set by social media services that we have added to the Site to enable you to share content with your friends and networks. They may track your browsing activity across different websites and build a profile of your interests.</p>

<p><strong>Purpose:</strong></p>
<ul>
    <li>Enabling social sharing buttons</li>
    <li>Tracking social media interactions</li>
    <li>Personalizing social media content and ads</li>
    <li>Integrating social media features and widgets</li>
</ul>
