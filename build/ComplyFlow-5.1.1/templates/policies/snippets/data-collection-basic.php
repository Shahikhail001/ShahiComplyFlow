<h3>Basic Information Collection</h3>
<p>We collect information that you provide directly to us, including:</p>
<ul>
    <li><strong>Account Information:</strong> Name, email address, username, and password when you create an account</li>
    <li><strong>Profile Information:</strong> Additional information you choose to provide in your profile</li>
    <li><strong>Communications:</strong> Information you provide when you contact us or participate in surveys</li>
</ul>

<p>We also automatically collect certain information when you use our website:</p>
<ul>
    <li><strong>Device Information:</strong> IP address, browser type, operating system</li>
    <li><strong>Usage Data:</strong> Pages viewed, time spent on pages, clickstream data</li>
    <li><strong>Location Data:</strong> General location based on your IP address</li>
</ul>
