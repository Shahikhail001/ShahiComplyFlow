<h3>PDPA (Singapore) Compliance Statement</h3>
<p>We comply with Singapore's Personal Data Protection Act 2012 (PDPA) for processing personal data of individuals in Singapore.</p>

<h4>Data Protection Obligations</h4>
<p>Under the PDPA, we adhere to the following data protection obligations:</p>

<h5>1. Consent Obligation</h5>
<p>We obtain your consent before collecting, using, or disclosing your personal data, unless an exception applies. Consent must be:</p>
<ul>
    <li>Provided voluntarily</li>
    <li>Based on clear notification of purposes</li>
    <li>Given with reasonable understanding of consequences</li>
</ul>

<h5>2. Purpose Limitation Obligation</h5>
<p>We collect, use, or disclose personal data only for purposes that:</p>
<ul>
    <li>A reasonable person would consider appropriate in the circumstances</li>
    <li>Have been notified to you</li>
</ul>

<h5>3. Notification Obligation</h5>
<p>We inform you of the purposes for collecting, using, or disclosing your personal data on or before obtaining your consent.</p>

<h5>4. Access and Correction Obligation</h5>
<p>Upon request, we provide you with:</p>
<ul>
    <li>Information about how your personal data has been used or disclosed within the past year</li>
    <li>The ability to correct errors or omissions in your personal data</li>
</ul>

<h5>5. Accuracy Obligation</h5>
<p>We make reasonable efforts to ensure your personal data is accurate and complete if it is likely to be disclosed to another organization or used to make a decision that affects you.</p>

<h5>6. Protection Obligation</h5>
<p>We protect personal data in our possession or control by making reasonable security arrangements to prevent:</p>
<ul>
    <li>Unauthorized access, collection, use, disclosure, copying, modification, or disposal</li>
    <li>Loss of any storage medium or device on which personal data is stored</li>
</ul>

<h5>7. Retention Limitation Obligation</h5>
<p>We cease to retain personal data, or remove the means by which it can be associated with you, as soon as it is reasonable to assume that:</p>
<ul>
    <li>The purpose for which the personal data was collected is no longer being served by retention</li>
    <li>Retention is no longer necessary for legal or business purposes</li>
</ul>

<h5>8. Transfer Limitation Obligation</h5>
<p>We do not transfer personal data to a country or territory outside Singapore except in accordance with the PDPA requirements.</p>

<h5>9. Openness Obligation</h5>
<p>We develop and implement policies and practices to meet PDPA obligations and make information about these policies and practices available upon request.</p>

<h4>Do Not Call (DNC) Registry</h4>
<p>We comply with Singapore's Do Not Call Registry and do not send marketing messages to numbers registered on the DNC Registry without consent.</p>

<h4>Data Protection Commissioner</h4>
<p>You have the right to file a complaint with the Personal Data Protection Commission (PDPC) of Singapore.</p>
