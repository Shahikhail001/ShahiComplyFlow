<h3>LGPD Compliance Statement</h3>
<p>We comply with Brazil's Lei Geral de Proteção de Dados (LGPD) for processing personal data of Brazilian residents.</p>

<h4>Legal Bases for Processing</h4>
<p>We process personal data based on:</p>
<ul>
    <li><strong>Consent:</strong> When you provide explicit consent</li>
    <li><strong>Contractual Compliance:</strong> To fulfill contractual obligations</li>
    <li><strong>Legal Obligation:</strong> To comply with legal or regulatory requirements</li>
    <li><strong>Legitimate Interest:</strong> For legitimate purposes that don't harm your rights</li>
</ul>

<h4>Data Protection Officer</h4>
<p>We have appointed a Data Protection Officer (Encarregado) responsible for:</p>
<ul>
    <li>Accepting communications from data subjects and authorities</li>
    <li>Providing guidance on data protection practices</li>
    <li>Ensuring compliance with LGPD requirements</li>
</ul>

<h4>International Data Transfers</h4>
<p>When transferring data outside Brazil, we ensure:</p>
<ul>
    <li>The destination country provides adequate data protection</li>
    <li>Appropriate contractual safeguards are in place</li>
    <li>You have provided specific consent for the transfer</li>
</ul>

<h4>National Data Protection Authority (ANPD)</h4>
<p>You have the right to lodge a complaint with the Autoridade Nacional de Proteção de Dados (ANPD) if you believe we have not complied with LGPD requirements.</p>

<h4>Data Sharing</h4>
<p>We maintain a record of data sharing activities and can provide information about:</p>
<ul>
    <li>Public and private entities with which we share data</li>
    <li>Purpose of data sharing</li>
    <li>Legal basis for sharing</li>
</ul>
