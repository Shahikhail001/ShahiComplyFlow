<h3>Hotjar Analytics and Feedback</h3>

<p>We use Hotjar to better understand our users' needs and optimize our Site experience. Hotjar provides behavior analytics and feedback tools that help us understand how visitors interact with our Site.</p>

<p><strong>Information Collected by Hotjar:</strong></p>
<ul>
    <li><strong>Mouse Movements and Clicks:</strong> How you move your mouse and where you click on our Site</li>
    <li><strong>Scrolling Behavior:</strong> How far you scroll down pages and which sections you view</li>
    <li><strong>Keystroke Patterns:</strong> Non-identifiable keystroke patterns (sensitive data like passwords is automatically excluded)</li>
    <li><strong>Device Information:</strong> Device type, browser, operating system, screen size, and viewport</li>
    <li><strong>Page Interactions:</strong> Which pages you visit, how long you stay, and navigation paths</li>
    <li><strong>Geographic Data:</strong> General location based on IP address (country and city level)</li>
    <li><strong>Referral Information:</strong> How you arrived at our Site (search engine, direct link, etc.)</li>
    <li><strong>Feedback Responses:</strong> Any feedback you voluntarily provide through Hotjar surveys or feedback widgets</li>
</ul>

<p><strong>How Hotjar Data Is Used:</strong></p>
<ul>
    <li><strong>Heatmaps:</strong> Visual representations of where users click, move, and scroll to identify popular and ignored areas</li>
    <li><strong>Session Recordings:</strong> Playback of anonymized user sessions to understand user behavior and identify usability issues</li>
    <li><strong>Conversion Funnels:</strong> Analyzing where users drop off in conversion processes to improve user experience</li>
    <li><strong>Form Analysis:</strong> Understanding which form fields cause users difficulty or abandonment</li>
    <li><strong>Feedback Polls:</strong> Collecting direct user feedback about their experience on specific pages</li>
    <li><strong>Surveys:</strong> Gathering user opinions and satisfaction ratings to guide improvements</li>
</ul>

<p><strong>Data Privacy and Anonymization:</strong></p>
<ul>
    <li><strong>IP Anonymization:</strong> Hotjar automatically anonymizes IP addresses so individual users cannot be identified</li>
    <li><strong>Sensitive Data Suppression:</strong> Hotjar automatically suppresses input fields containing sensitive information like passwords, credit card numbers, and social security numbers</li>
    <li><strong>Cookie Notice:</strong> Hotjar cookies are subject to our cookie consent banner, and recordings only occur with your consent where required by law</li>
    <li><strong>No Personal Identification:</strong> Hotjar does not collect personally identifiable information unless you voluntarily provide it in a feedback form</li>
</ul>

<p><strong>Hotjar Cookies Used:</strong></p>
<ul>
    <li><strong>_hjSessionUser_*:</strong> Identifies unique users across sessions (365-day expiration)</li>
    <li><strong>_hjSession_*:</strong> Holds current session data (30-minute expiration)</li>
    <li><strong>_hjIncludedInPageviewSample:</strong> Determines if user is included in data sampling (30-minute expiration)</li>
    <li><strong>_hjAbsoluteSessionInProgress:</strong> Detects user's first session (30-minute expiration)</li>
    <li><strong>_hjFirstSeen:</strong> Identifies first-time visitors (session duration)</li>
</ul>

<p><strong>Data Retention:</strong> Hotjar retains session recording data for 365 days by default, after which it is automatically deleted. Heatmap data is aggregated and anonymized. Survey responses may be retained longer if they contain valuable feedback, but any personally identifiable information is removed unless explicitly provided with consent.</p>

<p><strong>Opting Out of Hotjar Tracking:</strong></p>
<ul>
    <li><strong>Manage Cookie Preferences:</strong> Use our cookie consent banner to disable analytics cookies, including Hotjar</li>
    <li><strong>Do Not Track:</strong> Enable Do Not Track in your browser settings</li>
    <li><strong>Hotjar Opt-Out:</strong> Visit <a href="https://www.hotjar.com/policies/do-not-track/" target="_blank" rel="noopener">Hotjar's opt-out page</a> to install a cookie that prevents Hotjar from tracking you across all websites using Hotjar</li>
    <li><strong>Browser Extensions:</strong> Use privacy-focused browser extensions that block tracking scripts</li>
</ul>

<p><strong>Data Security and Compliance:</strong></p>
<ul>
    <li><strong>GDPR Compliance:</strong> Hotjar is fully GDPR compliant with a Data Processing Agreement in place</li>
    <li><strong>Encryption:</strong> All data transmission to Hotjar servers uses SSL/TLS encryption</li>
    <li><strong>Secure Storage:</strong> Data is stored in secure, access-controlled data centers within the EU or US (depending on your location)</li>
    <li><strong>Sub-Processors:</strong> Hotjar uses vetted sub-processors (like AWS, Google Cloud) with appropriate security and privacy safeguards</li>
    <li><strong>Data Transfers:</strong> International data transfers comply with EU Standard Contractual Clauses</li>
</ul>

