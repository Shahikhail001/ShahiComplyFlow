<h3>PDPL (Saudi Arabia) Compliance Statement</h3>
<p>We comply with the Saudi Arabian Personal Data Protection Law (PDPL) for processing personal data of individuals in the Kingdom of Saudi Arabia.</p>

<h4>Personal Data Processing Principles</h4>
<p>We process personal data in accordance with the following principles:</p>
<ul>
    <li><strong>Lawfulness and Fairness:</strong> Processing is conducted lawfully and fairly</li>
    <li><strong>Purpose Limitation:</strong> Data is collected for specified, explicit, and legitimate purposes</li>
    <li><strong>Data Minimization:</strong> Collection is limited to what is necessary for the purposes</li>
    <li><strong>Accuracy:</strong> Data is accurate and kept up to date</li>
    <li><strong>Storage Limitation:</strong> Data is retained only as long as necessary</li>
    <li><strong>Integrity and Confidentiality:</strong> Appropriate security measures are implemented</li>
    <li><strong>Accountability:</strong> We demonstrate compliance with PDPL requirements</li>
</ul>

<h4>Legal Basis for Processing</h4>
<p>We process personal data based on one or more of the following legal bases:</p>
<ul>
    <li>Your consent to the processing</li>
    <li>Performance of a contract to which you are a party</li>
    <li>Compliance with a legal obligation</li>
    <li>Protection of vital interests of the data subject or another person</li>
    <li>Performance of a task carried out in the public interest</li>
    <li>Legitimate interests pursued by us or a third party</li>
</ul>

<h4>Sensitive Personal Data</h4>
<p>We do not process sensitive personal data (data revealing racial or ethnic origin, political opinions, religious or philosophical beliefs, health data, genetic data, biometric data, or data concerning sexual orientation) unless:</p>
<ul>
    <li>You have given explicit consent</li>
    <li>Processing is necessary for substantial public interest</li>
    <li>Processing is authorized by law with appropriate safeguards</li>
</ul>

<h4>Your Rights Under PDPL</h4>
<p>You have the following rights regarding your personal data:</p>
<ul>
    <li><strong>Right to Information:</strong> Be informed about the collection and use of your personal data</li>
    <li><strong>Right of Access:</strong> Access your personal data and obtain information about its processing</li>
    <li><strong>Right to Rectification:</strong> Correct inaccurate or incomplete personal data</li>
    <li><strong>Right to Erasure:</strong> Request deletion of your personal data in certain circumstances</li>
    <li><strong>Right to Restriction:</strong> Restrict the processing of your personal data</li>
    <li><strong>Right to Data Portability:</strong> Receive your data in a structured, commonly used format</li>
    <li><strong>Right to Object:</strong> Object to the processing of your personal data</li>
    <li><strong>Right to Withdraw Consent:</strong> Withdraw consent at any time</li>
    <li><strong>Right to Lodge a Complaint:</strong> File a complaint with the Saudi Data & AI Authority (SDAIA)</li>
</ul>

<h4>Transparency Obligations</h4>
<p>When collecting your personal data, we provide you with the following information:</p>
<ul>
    <li>Our identity and contact details</li>
    <li>The purposes of processing</li>
    <li>The legal basis for processing</li>
    <li>Recipients or categories of recipients of the data</li>
    <li>Information about international transfers</li>
    <li>Retention period</li>
    <li>Your rights under the PDPL</li>
    <li>Right to lodge a complaint with SDAIA</li>
</ul>

<h4>Data Security Measures</h4>
<p>We implement appropriate technical and organizational measures to ensure a level of security appropriate to the risk, including:</p>
<ul>
    <li>Encryption of personal data</li>
    <li>Measures to ensure ongoing confidentiality, integrity, availability, and resilience</li>
    <li>Regular testing and assessment of security measures</li>
    <li>Incident response procedures</li>
</ul>

<h4>Data Protection Impact Assessment</h4>
<p>Where processing is likely to result in a high risk to your rights and freedoms, we carry out a data protection impact assessment before processing.</p>

<h4>Data Breach Notification</h4>
<p>In the event of a personal data breach, we will:</p>
<ul>
    <li>Notify the Saudi Data & AI Authority (SDAIA) without undue delay and, where feasible, within 72 hours</li>
    <li>Notify you if the breach is likely to result in a high risk to your rights and freedoms</li>
    <li>Document all personal data breaches</li>
</ul>

<h4>International Data Transfers</h4>
<p>We may transfer personal data outside Saudi Arabia only if:</p>
<ul>
    <li>The destination country ensures an adequate level of protection</li>
    <li>Appropriate safeguards are provided (such as standard contractual clauses)</li>
    <li>Specific derogations apply (such as your explicit consent)</li>
    <li>SDAIA has approved the transfer</li>
</ul>

<h4>Data Protection Officer</h4>
<p>We have appointed a Data Protection Officer (DPO) who is responsible for monitoring compliance with the PDPL. You can contact our DPO for any questions or concerns about how we handle your personal data.</p>

<h4>Children's Data</h4>
<p>We do not knowingly collect or process personal data of children under the age of 18 without verifiable parental or guardian consent, except where permitted by law.</p>

<h4>Supervisory Authority</h4>
