<h3>Managing Your Cookie Preferences</h3>
<p>You have several options for managing cookies:</p>

<h4>Browser Settings</h4>
<p>Most web browsers allow you to control cookies through their settings:</p>
<ul>
    <li><strong>Chrome:</strong> Settings > Privacy and security > Cookies and other site data</li>
    <li><strong>Firefox:</strong> Settings > Privacy & Security > Cookies and Site Data</li>
    <li><strong>Safari:</strong> Preferences > Privacy > Cookies and website data</li>
    <li><strong>Edge:</strong> Settings > Cookies and site permissions > Cookies and site data</li>
</ul>

<h4>Our Cookie Consent Tool</h4>
<p>You can manage your cookie preferences using our cookie consent banner. Click "Cookie Settings" at any time to modify your preferences.</p>

<h4>Opt-Out Links</h4>
<p>For specific types of cookies, you can opt-out directly:</p>
<ul>
    <li><strong>Google Analytics:</strong> <a href="https://tools.google.com/dlpage/gaoptout" target="_blank">Google Analytics Opt-out Browser Add-on</a></li>
    <li><strong>Advertising:</strong> <a href="http://optout.aboutads.info/" target="_blank">NAI Opt-Out</a> or <a href="http://optout.networkadvertising.org/" target="_blank">DAA Opt-Out</a></li>
</ul>

<p><strong>Note:</strong> If you block or delete cookies, some features of our website may not function properly.</p>
