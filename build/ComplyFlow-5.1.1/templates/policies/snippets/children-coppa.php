<h3>Children's Privacy (COPPA Compliance)</h3>
<p>Our website complies with the Children's Online Privacy Protection Act (COPPA). We take special precautions when collecting information from children under 13 years of age.</p>

<p><strong>Parental Consent:</strong> We obtain verifiable parental consent before collecting, using, or disclosing personal information from children. Parents can review their child's personal information, request deletion, and refuse further collection.</p>

<p><strong>Information We Collect from Children:</strong></p>
<ul>
    <li>Username and password</li>
    <li>Email address (parent's email for communication)</li>
    <li>Other information necessary for participation in our services</li>
</ul>

<p><strong>How We Use Children's Information:</strong> We only use children's information for the purposes for which it was collected, and we do not share it with third parties except as required by law or with parental consent.</p>

<p><strong>Parental Rights:</strong> Parents can contact us at any time to:</p>
<ul>
    <li>Review their child's personal information</li>
    <li>Request deletion of their child's information</li>
    <li>Refuse further collection of their child's information</li>
</ul>

<p>For more information about COPPA, visit the FTC's website at <a href="https://www.ftc.gov/coppa" target="_blank">www.ftc.gov/coppa</a>.</p>
