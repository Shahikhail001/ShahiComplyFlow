<h3>Essential Cookies</h3>
<p>We use cookies and similar tracking technologies to enhance your experience on our Site. Cookies are small text files stored on your device that help us:</p>

<ul>
    <li><strong>Session Management:</strong> Keep you logged in during your visit</li>
    <li><strong>Security:</strong> Protect against fraudulent activity and enhance security</li>
    <li><strong>Functionality:</strong> Remember your preferences and settings</li>
    <li><strong>Performance:</strong> Load pages faster and improve site performance</li>
</ul>

<p><strong>Types of Cookies We Use:</strong></p>
<ul>
    <li><strong>Strictly Necessary Cookies:</strong> Required for the website to function properly (cannot be disabled)</li>
    <li><strong>Functional Cookies:</strong> Remember your preferences and choices</li>
    <li><strong>Performance Cookies:</strong> Help us understand how visitors use our Site</li>
    <li><strong>Targeting Cookies:</strong> Used for advertising and marketing purposes</li>
</ul>

<p>You can control cookies through your browser settings. However, disabling certain cookies may affect the functionality of our Site.</p>
