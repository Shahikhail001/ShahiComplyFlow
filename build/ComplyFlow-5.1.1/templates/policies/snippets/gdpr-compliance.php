<h3>GDPR Compliance Statement</h3>
<p>We comply with the General Data Protection Regulation (GDPR) for processing personal data of individuals in the European Economic Area (EEA).</p>

<h4>Legal Basis for Processing</h4>
<p>We process your data based on:</p>
<ul>
    <li><strong>Consent:</strong> You have given clear consent for processing</li>
    <li><strong>Contract:</strong> Processing is necessary to fulfill a contract with you</li>
    <li><strong>Legal Obligation:</strong> We must comply with the law</li>
    <li><strong>Legitimate Interests:</strong> We have a legitimate reason that doesn't override your rights</li>
</ul>

<h4>Data Protection Officer</h4>
<p>We have appointed a Data Protection Officer (DPO) who is responsible for overseeing our data protection strategy and ensuring GDPR compliance.</p>

<h4>Data Transfers</h4>
<p>When we transfer data outside the EEA, we use:</p>
<ul>
    <li>Standard Contractual Clauses approved by the European Commission</li>
    <li>Adequacy decisions where the destination country provides adequate protection</li>
    <li>Binding Corporate Rules for transfers within our organization</li>
</ul>

<h4>Supervisory Authority</h4>
<p>You have the right to lodge a complaint with your local data protection authority if you believe we have not complied with GDPR requirements.</p>
