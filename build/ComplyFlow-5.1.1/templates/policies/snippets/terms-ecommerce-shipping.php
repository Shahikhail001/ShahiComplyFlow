</ul>

<h4>Lost or Damaged Shipments</h4>
<ul>
    <li><strong>Lost Packages:</strong> If a package is confirmed lost by the carrier, we will replace the order or issue a refund at our discretion</li>
    <li><strong>Damaged Items:</strong> If you receive a damaged item, contact us within 48 hours of delivery with photos of the damage. We will arrange for a replacement or refund</li>
    <li><strong>Stolen Packages:</strong> We are not responsible for packages stolen after delivery confirmation. We recommend requiring a signature for delivery if you have concerns about theft</li>
