<h3>PDPA (Thailand) Compliance Statement</h3>
<p>We comply with Thailand's Personal Data Protection Act B.E. 2562 (2019) (PDPA) for processing personal data of individuals in Thailand.</p>

<h4>Legal Basis for Processing</h4>
<p>We process personal data based on one or more of the following legal grounds:</p>
<ul>
    <li><strong>Consent:</strong> You have given consent for one or more specific purposes</li>
    <li><strong>Contract Performance:</strong> Processing is necessary for the performance of a contract to which you are party</li>
    <li><strong>Legal Obligation:</strong> Processing is necessary for compliance with the law</li>
    <li><strong>Vital Interests:</strong> Processing is necessary to protect your vital interests or those of another person</li>
    <li><strong>Public Interest:</strong> Processing is necessary for the performance of a task carried out in the public interest</li>
    <li><strong>Legitimate Interests:</strong> Processing is necessary for the purposes of legitimate interests pursued by us or a third party</li>
</ul>

<h4>Data Subject Rights</h4>
<p>Under the Thailand PDPA, you have the following rights:</p>
<ul>
    <li><strong>Right to Withdraw Consent:</strong> You can withdraw your consent at any time</li>
    <li><strong>Right to Access:</strong> You can request access to your personal data and information about how we process it</li>
    <li><strong>Right to Data Portability:</strong> You can request to receive your personal data in a structured, commonly used, and machine-readable format</li>
    <li><strong>Right to Object:</strong> You can object to the processing of your personal data for direct marketing purposes</li>
    <li><strong>Right to Erasure:</strong> You can request deletion of your personal data in certain circumstances</li>
    <li><strong>Right to Restriction:</strong> You can request restriction of processing of your personal data</li>
    <li><strong>Right to Rectification:</strong> You can request correction of inaccurate or incomplete personal data</li>
    <li><strong>Right to Complaint:</strong> You can lodge a complaint with the Personal Data Protection Committee</li>
</ul>

<h4>Data Protection Officer (DPO)</h4>
<p>We have appointed a Data Protection Officer who is responsible for monitoring compliance with the PDPA and serving as a contact point for data subjects and the Personal Data Protection Committee.</p>

<h4>Cross-Border Data Transfer</h4>
<p>When transferring personal data outside Thailand, we ensure that:</p>
<ul>
    <li>The destination country has adequate data protection standards as prescribed by the Personal Data Protection Committee, or</li>
    <li>Appropriate safeguards are in place, such as standard contractual clauses, binding corporate rules, or other mechanisms approved by the Committee</li>
</ul>

<h4>Data Breach Notification</h4>
<p>In the event of a personal data breach that is likely to affect your rights and freedoms, we will:</p>
<ul>
    <li>Notify the Personal Data Protection Committee without undue delay</li>
    <li>Notify you without undue delay, unless the breach is unlikely to result in a risk to your rights and freedoms</li>
</ul>

