<h3>Subscriptions and Recurring Billing</h3>

<p>Some products or services are billed on a subscription basis ("Subscriptions"). You will be billed in advance on a recurring and periodic basis ("Billing Cycle"). Billing Cycles are set based on the type of subscription plan you select when purchasing a Subscription.</p>

<h4>Automatic Renewal</h4>
<ul>
    <li><strong>Auto-Renewal:</strong> Subscriptions automatically renew at the end of each Billing Cycle unless you cancel before the renewal date</li>
    <li><strong>Payment Method:</strong> Your payment method will be automatically charged at the beginning of each Billing Cycle for the applicable subscription fees</li>
    <li><strong>Renewal Notice:</strong> We will notify you before your subscription renews, giving you the opportunity to cancel if desired</li>
</ul>

<h4>Subscription Changes</h4>
<ul>
    <li><strong>Upgrade:</strong> You may upgrade your subscription at any time. Upgrades will be prorated and charged immediately</li>
    <li><strong>Downgrade:</strong> You may downgrade your subscription at any time. Downgrades will take effect at the beginning of the next Billing Cycle</li>
    <li><strong>Price Changes:</strong> We may change subscription fees at any time. We will notify you at least 30 days before any price increase takes effect. Your continued use after the price change constitutes acceptance of the new price</li>
</ul>

<h4>Cancellation</h4>
<ul>
    <li><strong>How to Cancel:</strong> You may cancel your subscription at any time through your account settings or by contacting us at {{CONTACT_EMAIL}}</li>
    <li><strong>Effective Date:</strong> Cancellations take effect at the end of your current Billing Cycle. You will retain access to your subscription benefits until that date</li>
    <li><strong>No Refunds:</strong> We do not provide refunds or credits for partial Billing Cycles. If you cancel, you will not be charged for subsequent Billing Cycles</li>
    <li><strong>Reactivation:</strong> You may reactivate a canceled subscription at any time, subject to current pricing and terms</li>
</ul>

<h4>Failed Payments</h4>
<p>If we are unable to process your payment for a subscription renewal:</p>
<ul>
    <li>We will attempt to charge your payment method multiple times over a grace period</li>
    <li>We will notify you via email about the failed payment</li>
    <li>Your subscription may be suspended if payment cannot be processed</li>
    <li>Your subscription will be canceled if payment is not successfully processed within the grace period</li>
</ul>

<h4>Free Trials</h4>
<p>If applicable, we may offer a free trial period for certain subscriptions:</p>
<ul>
    <li><strong>Duration:</strong> The free trial period will be clearly stated at the time of signup</li>
    <li><strong>Payment Required:</strong> You must provide payment information to start a free trial</li>
    <li><strong>Automatic Conversion:</strong> Unless you cancel before the trial ends, you will automatically be charged for the subscription at the end of the trial period</li>
    <li><strong>One Trial Per User:</strong> Free trials are limited to one per user. We reserve the right to deny free trials to users who have previously subscribed</li>
</ul>
