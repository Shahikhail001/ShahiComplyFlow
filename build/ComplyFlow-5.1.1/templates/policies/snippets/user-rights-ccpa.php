<h3>CCPA Rights (California Residents)</h3>
<p>If you are a California resident, you have specific rights under the California Consumer Privacy Act (CCPA):</p>
<ul>
    <li><strong>Right to Know:</strong> You can request information about the personal data we collect, use, disclose, and sell</li>
    <li><strong>Right to Delete:</strong> You can request deletion of your personal information</li>
    <li><strong>Right to Opt-Out:</strong> You can opt-out of the sale of your personal information</li>
    <li><strong>Right to Non-Discrimination:</strong> We will not discriminate against you for exercising your CCPA rights</li>
</ul>

<p><strong>Do Not Sell My Personal Information:</strong> We do not sell your personal information to third parties. However, we may share information with service providers and business partners as described in this policy.</p>

<p>To exercise your CCPA rights, please contact us or use our <a href="/data-subject-request">Data Subject Request Portal</a>.</p>
